package com.looigi.loovf.griglia;

public class ImageListForRecycler {
    private String image_title;
    private String image_url;
    private String image_id;
    private String image_dime;
    private String image_date;
    private String image_category;
    private String image_number;

    public String getImage_id() {
        return image_id;
    }

    public void setImage_id(String image_id) {
        this.image_id = image_id;
    }

    public String getImage_title() {
        return image_title;
    }

    public void setImage_title(String android_version_name) {
        this.image_title = android_version_name;
    }

    public String getImage_number() {
        return image_number;
    }

    public void setImage_number(String image_number) {
        this.image_number = image_number;
    }

    public String getImage_category() {
        return image_category;
    }

    public void setImage_category(String image_category) {
        this.image_category = image_category;
    }

    public String getImage_date() {
        return image_date;
    }

    public void setImage_date(String image_date) {
        this.image_date = image_date;
    }

    public String getImage_dime() {
        return image_dime;
    }

    public void setImage_dime(String image_dime) {
        this.image_dime = image_dime;
    }

    public String getImage_URL() {
        return image_url;
    }

    public void setImage_URL(String android_image_url) {
        this.image_url = android_image_url;
    }
}

